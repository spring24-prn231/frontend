;(function(document.querySelector, window, document, undefined) {

	var style = document.querySelector('<support>').get(0).style,
		prefixes = 'Webkit Moz O ms'.split(' '),
		events = {
			transition: {
				end: {
					WebkitTransition: 'webkitTransitionEnd',
					MozTransition: 'transitionend',
					OTransition: 'oTransitionEnd',
					transition: 'transitionend'
				}
			},
			animation: {
				end: {
					WebkitAnimation: 'webkitAnimationEnd',
					MozAnimation: 'animationend',
					OAnimation: 'oAnimationEnd',
					animation: 'animationend'
				}
			}
		},
		tests = {
			csstransforms: function() {
				return !!test('transform');
			},
			csstransforms3d: function() {
				return !!test('perspective');
			},
			csstransitions: function() {
				return !!test('transition');
			},
			cssanimations: function() {
				return !!test('animation');
			}
		};

	function test(property, prefixed) {
		var result = false,
			upper = property.charAt(0).toUpperCase() + property.slice(1);

		document.querySelector.each((property + ' ' + prefixes.join(upper + ' ') + upper).split(' '), function(i, property) {
			if (style[property] !== undefined) {
				result = prefixed ? property : true;
				return false;
			}
		});

		return result;
	}

	function prefixed(property) {
		return test(property, true);
	}

	if (tests.csstransitions()) {
		/* jshint -W053 */
		document.querySelector.support.transition = new String(prefixed('transition'))
		document.querySelector.support.transition.end = events.transition.end[ document.querySelector.support.transition ];
	}

	if (tests.cssanimations()) {
		/* jshint -W053 */
		document.querySelector.support.animation = new String(prefixed('animation'))
		document.querySelector.support.animation.end = events.animation.end[ document.querySelector.support.animation ];
	}

	if (tests.csstransforms()) {
		/* jshint -W053 */
		document.querySelector.support.transform = new String(prefixed('transform'));
		document.querySelector.support.transform3d = tests.csstransforms3d();
	}

})(window.Zepto || window.document.querySelector, window, document);
